// NAME GENERATION ENGINE
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

class NameGenerationEngine {
  constructor() {
    this.usedNames = new Set();
    this.recentNames = []; // Track recent for cooldown
    this.recentCooldownMs = 5 * 60 * 1000; // 5 minute cooldown
  }

  /**
   * Get name format (Bengali, English, or Banglish)
   */
  getFormat() {
    const rand = Math.random();
    if (rand < 0.35) return 'bengali'; // 35% Bengali
    if (rand < 0.65) return 'english'; // 30% English
    return 'banglish'; // 35% Banglish
  }

  /**
   * Convert Bengali to Banglish (simplified transliteration)
   */
  bengaliToBanglish(bengaliText) {
    const map = {
      'আ': 'A', 'ই': 'I', 'উ': 'U', 'ঋ': 'Ri', 'এ': 'E', 'ঐ': 'Oi',
      'ও': 'O', 'ঔ': 'Ou', 'ক': 'K', 'খ': 'Kh', 'গ': 'G', 'ঘ': 'Gh',
      'ঙ': 'Ng', 'চ': 'Ch', 'ছ': 'Chh', 'জ': 'J', 'ঝ': 'Jh', 'ঞ': 'Ny',
      'ট': 'T', 'ঠ': 'Th', 'ড': 'D', 'ঢ': 'Dh', 'ণ': 'N', 'ত': 'T',
      'থ': 'Th', 'দ': 'D', 'ধ': 'Dh', 'ন': 'N', 'প': 'P', 'ফ': 'Ph',
      'ব': 'B', 'ভ': 'Bh', 'ম': 'M', 'য': 'Y', 'র': 'R', 'ল': 'L',
      'শ': 'Sh', 'ষ': 'Sh', 'স': 'S', 'হ': 'H', 'ড়': 'R', 'ঢ়': 'Rh',
      'য়': 'Y', 'ৎ': 't',
      'া': 'a', 'ি': 'i', 'ী': 'i', 'ু': 'u', 'ূ': 'u', 'ৃ': 'ri',
      'ে': 'e', 'ৈ': 'oi', 'ো': 'o', 'ৌ': 'ou'
    };

    return bengaliText.split('').map(char => map[char] || char).join('');
  }

  /**
   * Generate full name with validation
   */
  generateName(isMuslim = null) {
    // Auto-detect religion if not specified (55% Muslim, 45% Hindu)
    if (isMuslim === null) {
      isMuslim = Math.random() < 0.55;
    }

    const format = this.getFormat();
    let firstName, lastName;

    if (isMuslim) {
      // Muslim names
      const gender = Math.random() < 0.5;
      firstName = gender
        ? MUSLIM_FIRST_NAMES_MALE[Math.floor(Math.random() * MUSLIM_FIRST_NAMES_MALE.length)]
        : MUSLIM_FIRST_NAMES_FEMALE[Math.floor(Math.random() * MUSLIM_FIRST_NAMES_FEMALE.length)];
      lastName = MUSLIM_LAST_NAMES[Math.floor(Math.random() * MUSLIM_LAST_NAMES.length)];
    } else {
      // Hindu names
      const gender = Math.random() < 0.5;
      firstName = gender
        ? HINDU_FIRST_NAMES_MALE[Math.floor(Math.random() * HINDU_FIRST_NAMES_MALE.length)]
        : HINDU_FIRST_NAMES_FEMALE[Math.floor(Math.random() * HINDU_FIRST_NAMES_FEMALE.length)];
      lastName = HINDU_LAST_NAMES[Math.floor(Math.random() * HINDU_LAST_NAMES.length)];
    }

    // Format name based on selected format
    let displayName;
    if (format === 'bengali') {
      displayName = `${firstName} ${lastName}`;
    } else if (format === 'english') {
      displayName = `${this.bengaliToBanglish(firstName)} ${this.bengaliToBanglish(lastName)}`;
    } else {
      // Banglish - use Bengali names, convert to Banglish
      displayName = `${this.bengaliToBanglish(firstName)} ${this.bengaliToBanglish(lastName)}`;
    }

    // Validate: Check for duplicates and recent usage
    if (this.isDuplicate(displayName) || this.isRecentlyUsed(displayName)) {
      // Retry with recursion (max 10 attempts to prevent infinite loop)
      if ((this.usedNames.size + this.recentNames.length) < 1000) {
        return this.generateName(isMuslim);
      } else {
        // Fallback to timestamp-based unique name
        return `User_${Date.now().toString().slice(-6)}`;
      }
    }

    // Mark as used
    this.usedNames.add(displayName);
    this.recentNames.push({ name: displayName, timestamp: Date.now() });

    // Clean old recent names (older than cooldown)
    const now = Date.now();
    this.recentNames = this.recentNames.filter(item => (now - item.timestamp) < this.recentCooldownMs);

    return displayName;
  }

  /**
   * Check if name is duplicate
   */
  isDuplicate(name) {
    return this.usedNames.has(name);
  }

  /**
   * Check if name was recently used (cooldown period)
   */
  isRecentlyUsed(name) {
    const now = Date.now();
    return this.recentNames.some(item => item.name === name && (now - item.timestamp) < this.recentCooldownMs);
  }

  /**
   * Get engine stats
   */
  getStats() {
    return {
      totalGenerated: this.usedNames.size,
      recentCount: this.recentNames.length,
      cooldownMs: this.recentCooldownMs
    };
  }

  /**
   * Reset engine (for testing)
   */
  reset() {
    this.usedNames.clear();
    this.recentNames = [];
  }
}

// Export singleton instance
const nameEngine = new NameGenerationEngine();

